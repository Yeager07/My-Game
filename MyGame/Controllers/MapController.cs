        public static void DrawMap(Graphics g)
        {
            cellLimitX = GetWidth() / cellSizeX;
            cellLimitY = GetHeight() / cellSizeY;

            for (int i = 0; i < cellLimitY; i++)
            {
                for (int j = 0; j < cellLimitX; j++)
                {
                    if (map[i, j] == 0)
                    {
                        g.DrawImage(new Bitmap(@"..\..\..\Sprites\umen.png"), new Rectangle(new Point(cellSizeX * j, cellSizeY * i), new Size(cellSizeX, cellSizeY)), 0, 0, 59, 59, GraphicsUnit.Pixel);
                    }
                    else
                        g.DrawImage(imgMap,
                        new Rectangle(new Point(cellSizeX * j, cellSizeY * i), new Size(cellSizeX, cellSizeY)),
                        cellSizeX * j, cellSizeY * i,
                        cellSizeX, cellSizeY,
                        GraphicsUnit.Pixel);
                }
            }
        }

