        public static void DrawMap(Graphics g)
        {
            cellLimitX = imgMap.Size.Width / cellSizeX;
            cellLimitY = imgMap.Size.Height / cellSizeY;

            for (int i = 0; i < cellLimitX; i++)
            {
                for (int j = 0; j < cellLimitY; j++)
                {
                    g.DrawImage(imgMap,
                        new Rectangle(new Point(cellSizeX * i, cellSizeY * j), new Size(cellSizeX, cellSizeY)),
                        cellSizeX * i, cellSizeY * j,
                        cellSizeX, cellSizeY,
                        GraphicsUnit.Pixel);
                }
            }
        }

